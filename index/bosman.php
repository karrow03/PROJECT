<h1> Bosman Szczeciński </h1>

<p> Browar Bosman obchodził w 2018 roku swoje 170-te urodziny.<br>

Historia browaru zaczyna się w 1848 roku. Kupiec Julius Albrecht Weidmann uruchomił wtedy w Szczecinie browar z własną słodownią, lodownią i ujęciem wody.

Prawdziwy rozkwit zakładu zlokalizowanego w dzisiejszej dzielnicy Pomorzany nastąpił po przejęciu go przez rodzinę Bohrisch. Nowi właściciele rozbudowali i unowocześnili browar. Za ich sprawą zbudowano nową warzelnię, która istnieje do dziś i wciąż stanowi dla nas powód do dumy.

W 1997 roku browar stał się własnością niemieckiego koncernu Bitburger Brauerei Th. Simnon GmbH, a następnie w 2001 roku został kupiony przez duńskiego Carlsberga. </p>

