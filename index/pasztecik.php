<h1> Paszteciki </h1>

<p> Wyobraźcie sobie pączka, ale w wersji wytrwanej z nadzieniem mięsnym. <br> Tak w skrócie można opisać tradycyjne paszteciki szczecińskie – niezwykle kaloryczne, ale za to rozkosznie pysznie. Właśnie zrobiliśmy je naszej kuchni.

Tak zrobione paszteciki były gastronomicznym przebojem w PRL. W każdym większym mieście był punkt, gdzie można było zjeść je na miejscu lub kupić na wynos – oczywiście z obowiązkowym barszczykiem. Ojczyzną pasztecików jest jednak Szczecin, a one same w 2010 roku zostały wpisane na listę produktów tradycyjnych. </p>

